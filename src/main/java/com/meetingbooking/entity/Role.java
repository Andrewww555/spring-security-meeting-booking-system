package com.meetingbooking.entity;

/**
 * Перечисление ролей пользователей в системе
 */
public enum Role {
    /**
     * Администратор - полный доступ ко всем функциям
     */
    ADMIN,

    /**
     * Обычный пользователь - может просматривать комнаты и создавать бронирования
     */
    USER,

    /**
     * VIP пользователь - все права обычного пользователя + доступ к VIP комнатам
     */
    VIP_USER
}